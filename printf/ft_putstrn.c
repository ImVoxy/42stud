#include "libft.h"

int	ft_putstrn(char *s)
{
	unsigned int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
		return (ft_strlen(s));
	}
	return (0);
}
